// Bitwise Shift Operators (<<, >>, >>>)

class BitShift {
    public static void main(String[] args) {
        int v = 20;
        int n = 2;

        System.out.println("Left Shift (v << n): " + (v << n));
        System.out.println("Right Shift (v >> n): " + (v >> n));
        System.out.println("Unsigned Right Shift (v >>> n): " + (v >>> n));
    }
}
