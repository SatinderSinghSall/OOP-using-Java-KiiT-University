// For Loop:

public class ForLoop {
    public static void main(String[] args) {
        String name = "Satinder Singh Sall";
        for(int i = 1; i <= 10; i ++) {
            System.out.println(i + ". " + name);
        }

        System.out.println();

        for(int i = 1; i <= 20; i ++) {
            System.out.println("Number: " + i);
        }
    }
}
